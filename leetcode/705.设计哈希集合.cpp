#include "vector"
using namespace std;

// @lc code=start
class MyHashSet
{
private:
    vector<bool> hvec = vector<bool>(1000000 + 1, false);

public:
    /** Initialize your data structure here. */
    MyHashSet()
    {
    }

    void add(int key)
    {
        hvec[key] = true;
    }

    void remove(int key)
    {
        hvec[key] = false;
    }

    /** Returns true if this set contains the specified element */
    bool contains(int key)
    {
        return hvec[key];
    }
};

